const rock = {
  beats: ["C", "Z"],
  losesTo: ["B", "Y"],
  score: 1,
};
const paper = {
  beats: ["A", "X"],
  losesTo: ["C", "Z"],
  score: 2,
};
const scissors = {
  beats: ["B", "Y"],
  losesTo: ["A", "X"],
  score: 3,
};
const weapons = {
  A: rock,
  B: paper,
  C: scissors,
  X: rock,
  Y: paper,
  Z: scissors,
};

const rockPaperScissors = (guide) => {
  const rounds = guide.split("\n").filter((round) => round);

  const scores = rounds.map(compileScores);

  const totalScore = scores.reduce((acc, score) => acc + score, 0);

  const newScores = rounds.map(compileNewTotalScore);

  const newTotalScore = newScores.reduce((acc, score) => acc + score, 0);

  return {totalScore, newTotalScore};
};

const compileScores = (round) => {
  const plays = round.split(" ");
  const opponentPlay = plays[0];
  const myPlay = plays[1];
  const playScore = compileWinLooseScore(myPlay, opponentPlay);

  const score = weapons[myPlay].score + playScore;

  return score;
};

const compileWinLooseScore = (myPlay, opponentPlay) => {
  console.log(myPlay, opponentPlay);
  const win = weapons[myPlay].beats.includes(opponentPlay);
  const lose = weapons[myPlay].losesTo.includes(opponentPlay);

  if (win) return 6;
  if (lose) return 0;
  return 3;
};

const compileNewTotalScore = (round) => {
  const inputs = round.split(" ");
  const opponentPlay = inputs[0];
  const outcome = inputs[1];
  const myPlay = () => {
    if (outcome === "X") return weapons[opponentPlay].beats[0];
    if (outcome === "Y")
      return ["A", "B", "C"].filter(
        (play) =>
          !weapons[opponentPlay].beats.includes(play) &&
          !weapons[opponentPlay].losesTo.includes(play)
      )[0];
    if (outcome === "Z") return weapons[opponentPlay].losesTo[0];
  };

  const playScore = compileWinLooseScore(myPlay(), opponentPlay);

  const score = weapons[myPlay()].score + playScore;

  return score;
};

